package com.example.magicofmath;

public class MyPointPaporot {

    private double numberPaporot;
    private double pointXcoordinatePaporot;
    private double pointYcoordinatePaporot;

    public MyPointPaporot() {
    }

    public MyPointPaporot(double numberPaporot, double pointXcoordinatePaporot, double pointYcoordinatePaporot) {
        this.numberPaporot = numberPaporot;
        this.pointXcoordinatePaporot = pointXcoordinatePaporot;
        this.pointYcoordinatePaporot = pointYcoordinatePaporot;
    }

    public double getNumberPaporot() {
        return numberPaporot;
    }

    public void setNumberPaporot(double numberPaporot) {
        this.numberPaporot = numberPaporot;
    }

    public double getPointXcoordinatePaporot() {
        return pointXcoordinatePaporot;
    }

    public void setPointXcoordinatePaporot(double pointXcoordinatePaporot) {
        this.pointXcoordinatePaporot = pointXcoordinatePaporot;
    }

    public double getPointYcoordinatePaporot() {
        return pointYcoordinatePaporot;
    }

    public void setPointYcoordinatePaporot(double pointYcoordinatePaporot) {
        this.pointYcoordinatePaporot = pointYcoordinatePaporot;
    }
}
